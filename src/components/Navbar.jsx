import { useEffect, useState } from "react";
import Button from "./Button";
import { Menu, SquareChevronUp } from "lucide-react";

const navLinks = [
  { href: "#hero", label: "Home" },
  { href: "#about", label: "About" },
  { href: "#projects", label: "Projects" },
  { href: "#contact", label: "Contact" }
];

function Navbar() {
  const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false);
  const [isScrolled, setIscrolled] = useState(false);



  useEffect(() => {
    const handleScroll = () => {
      setIscrolled(window.scrollY > 50);
    };
    window.addEventListener("scroll", handleScroll);

    return () => window.removeEventListener("scroll", handleScroll);
  }, []);



  return (
    <header className={`fixed top-0 left-0 right-0 transition-all duration-500 ${isScrolled? "glass-strong" : "bg-transparent p-5 "} z-50`}>
      <nav className="container mx-auto px-6 flex items-center justify-between py-2">
        {/* LEFT SIDE */}
        <a
          href="#"
          className="text-xl font-bold tracking-tight hover:text-primary"
        >
          AW<span className="text-primary">.</span>
        </a>

        {/* DESKTOP NAV */}
        <div className="hidden md:flex items-center gap-1">
          <div className="glass rounded-full px-2 py-1 flex items-center gap-1">
            {navLinks.map((link, index) => (
              <a
                href={link.href}
                key={index}
                className="px-4 py-2 text-sm text-muted-foreground hover:text-foreground rounded-full hover:bg-surface"
              >
                {link.label}
              </a>
            ))}
          </div>
        </div>

        {/* CONATCT ME BUTTON */}
        <div className="hidden md:block">
          <Button size="sm">Contact Me</Button>
        </div>

        {/* HAMBURGER NAV */}
        <button
          className="md:hidden p-2 text-foreground cursor-pointer"
          onClick={() => setIsMobileMenuOpen((prev) => !prev)}
        >
          {isMobileMenuOpen ? (
            <SquareChevronUp size={24} />
          ) : (
            <Menu size={24} />
          )}
        </button>
      </nav>

      {/* MOBILE NAV */}
      {isMobileMenuOpen && (
        <div className="md:hidden glass-strong animate-fade-in">
          <div className="container mx-auto px-6 py-6 flex flex-col gap-4">
            {navLinks.map((link, index) => (
              <a
                href={link.href}
                key={index}
                onClick={() => setIsMobileMenuOpen(false)}
                className="text-lg text-muted-foreground hover:text-foreground py-2"
              >
                {link.label}
              </a>
            ))}

            <Button onClick={() => setIsMobileMenuOpen(false)}>
              Contact Me
            </Button>
          </div>
        </div>
      )}
    </header>
  );
}

export default Navbar;
